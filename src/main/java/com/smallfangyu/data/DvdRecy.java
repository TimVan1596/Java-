package com.smallfangyu.data;

import java.util.List;

public class DvdRecy {
    private int show;
    private String sta;
    private List<DVD> dvd;

    public int getShow() {
        return show;
    }

    public void setShow(int show) {
        this.show = show;
    }

    public String getSta() {
        return sta;
    }

    public void setSta(String sta) {
        this.sta = sta;
    }

    public List<DVD> getDvd() {
        return dvd;
    }

    public void setDvd(List<DVD> dvd) {
        this.dvd = dvd;
    }
}

